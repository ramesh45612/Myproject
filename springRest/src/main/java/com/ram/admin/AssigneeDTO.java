package com.ram.admin;

public class AssigneeDTO {

	String assigneeId;
	String assigneeName;
	String assigneeType;
	/**
	 * @return the assigneeId
	 */
	public String getAssigneeId() {
		return assigneeId;
	}
	/**
	 * @param assigneeId the assigneeId to set
	 */
	public void setAssigneeId(String assigneeId) {
		this.assigneeId = assigneeId;
	}
	/**
	 * @return the assigneeName
	 */
	public String getAssigneeName() {
		return assigneeName;
	}
	/**
	 * @param assigneeName the assigneeName to set
	 */
	public void setAssigneeName(String assigneeName) {
		this.assigneeName = assigneeName;
	}
	/**
	 * @return the assigneeType
	 */
	public String getAssigneeType() {
		return assigneeType;
	}
	/**
	 * @param assigneeType the assigneeType to set
	 */
	public void setAssigneeType(String assigneeType) {
		this.assigneeType = assigneeType;
	}
	
}
